public class ConInit {

    int numOne = 0;

    // Constructors and initialization blocks

    // Constructor
    ConInit() {

        numOne = 5; // reassigns new value when a new object is made

    }

    // Initialization block 1
    {

        numOne = 3;

    }

    // Initialization block 2
    {

        numOne = 1;

    }

}
