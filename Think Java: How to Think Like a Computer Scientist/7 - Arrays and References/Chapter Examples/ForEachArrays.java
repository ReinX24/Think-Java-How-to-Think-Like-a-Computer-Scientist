public class ForEachArrays {

    public static void main(String[] args) {

        /*
         * For each loop is another loop that can be used to iterate through an array.
         * 
         */

        int[] numValues = {1, 3, 5, 7, 9};
        
        for (int eachValue : numValues) {

            System.out.println(eachValue);

        }

    }

}
