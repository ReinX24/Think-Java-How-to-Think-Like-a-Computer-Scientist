public class IncrementDecrement {

    public static void main(String[] args) {

        int i = 1;

        while (i <= 5) {

            // System.out.println(i); // prints 1
            i++; // increments i (+1)

        }

        // Cheer while loop

        int j = 2;

        while (j <= 8) {

            System.out.print(j + ", "); // prints numbers
            j += 2;

        }

        System.out.println("Who do we appreciate?");

    }

}